    def post_setup(cls):
        """Post setup configuration."""
        super().post_setup()

        # Force logger propagation to allow caplog to work
        # see https://github.com/pytest-dev/pytest/issues/3697
        cls.LOGGING["loggers"]["core"]["propagate"] = True


